package Operadores;

public class Aritimeticos {

	public static void main(String[] args) {

		System.out.println(2 + 3);

		var x = 34.56;
		double y = 2.2;
		
		System.out.println(x + y);
		System.out.println(x - y);
		System.out.println(x * y);
		System.out.println(x / y);
		
		int a = 8;
		int b = 3;
		
		System.out.println(a + b);
		System.out.println(a - b);
		System.out.println(a * b);
		System.out.println(a / b);
		
		System.out.println(a % b);
		System.out.println(8 % 3);
		
		double p = 8;
		double q = 3;
		
		System.out.println(p / q);
		System.out.println(a + b - p / q);
		
		
				
	
		

	}
}
